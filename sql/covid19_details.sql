create table coviddetails(
	iso_code varchar(36) comment '國碼',
    continent varchar(36) comment '洲名',
    location varchar(36) comment '國家',
    date_t varchar(36) comment '統計日期',
    total_cases varchar(36) comment '總確診數',
    new_cases varchar(36) comment '新增確診數',
    new_cases_smoothed varchar(36) comment '七天移動平均新增確診數',
    total_deaths varchar(36) comment '總死亡數',
    new_deaths varchar(36) comment '新增死亡數',
    new_deaths_smoothed varchar(36) comment '七天移動平均新增死亡數',
    total_cases_per_million varchar(36) comment '每百萬人確診數',
    total_deaths_per_million varchar(36) comment '每百萬人死亡數',
    reproduction_rate varchar(36) comment '傳染率',
    total_tests varchar(36) comment '總檢驗件數',
    new_tests varchar(36) comment '新增檢驗件數',
    total_tests_per_thousand varchar(36) comment '每千人檢驗件數',
    new_tests_smoothed varchar(36) comment '七天移動平均新增檢驗件數',
    positive_rate varchar(36) comment '陽性率',
    tests_per_case varchar(36) comment '每確診案例相對檢驗數量',
    total_vaccinations varchar(36) comment '疫苗總接種總劑數',
    people_fully_vaccinated varchar(36) comment '疫苗總接種人數',
    new_vaccinations varchar(36) comment '疫苗新增接種劑數',
    new_vaccinations_smoothed varchar(36) comment '七天移動平均疫苗新增接種劑數',
    total_vaccinations_per_hundred varchar(36) comment '每百人接種疫苗劑數',
    people_fully_vaccinated_per_hundred varchar(36) comment '每百人接種疫苗人數',
    stringency_index varchar(36) comment '疫情控管指數',
    population varchar(36) comment '總人口數',
    median_age varchar(36) comment '中位數年紀',
    aged_70_older varchar(36) comment '70歲以上人口比例',
    life_expectancy varchar(36) comment '平均壽命',
    Recovered varchar(36) comment '解除隔離數',
    Unblock varchar(36) comment '解封指數'
);